; 在屏幕的第12行，显示data段中以0结尾的字符串。用7CH模拟LOOP的功能

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	DB 'conversation', 0
DATA ENDS

CODE SEGMENT
	START:
	MOV AX, DATA
	MOV DS, AX
	MOV AX, 0B800H
	MOV ES, AX
	MOV SI, 0000H
	MOV DI, 12 * 0A0H
	
	S:
	MOV AL, [SI]
	CMP AL, 00H
	JZ ZERO
	MOV ES:[DI], AL
	MOV BYTE PTR ES:[DI].01H, 05H
	INC SI
	ADD DI, 0002H
	MOV BX, OFFSET S - OFFSET ZERO
	INT 7CH

	ZERO:
	MOV AX, 4C00H
	INT 21H
CODE ENDS
	END START
