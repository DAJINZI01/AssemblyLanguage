; 任务：编写子程序，以十六进制的形式在屏幕中间显示给定的 byte型数据

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	TABLE DB "0123456789ABCEDF"
DATA ENDS

CODE SEGMENT

	START:
		MOV AX, DATA
		MOV DS, AX
	
		MOV AL, 1FH
		CALL SHOW_16
		; MAIN RETURN
		MOV AX, 4C00H
		INT 21H
		
	SHOW_16:
		PUSH BX
		PUSH CX
		PUSH ES
		; DIVIDE AL INTO AH AL
		MOV AH, AL
		MOV CL, 04H
		SHR AH, CL
		AND AL, 0FH
		
		; INITIATE SCREEN MEMERY 
		MOV BX, 0B800H
		MOV ES, BX
		MOV DI, 0A0H * 12 + 50H
		
		MOV BL, AH
		MOV BH, 00H
		MOV AH, TABLE[BX]
		MOV ES:[DI], AH
		MOV BYTE PTR ES:[DI+1], 0AH
		
		ADD DI, 0002H
		
		MOV BL, AL
		MOV BH, 00H
		MOV AL, TABLE[BX]
		MOV ES:[DI], AL
		MOV BYTE PTR ES:[DI+1], 09H
		
		PUSH ES
		PUSH CX
		PUSH BX
		RET
CODE ENDS
END START