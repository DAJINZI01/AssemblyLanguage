; 实验9 根据材料编程
; 编程：在屏幕中间分别显示绿色/绿低红色/白底蓝色的字符串 'welcome to masm!'

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	DB 'welcome to masm!'
DATA ENDS

STACK SEGMENT 
	DB 16 DUP (0)
STACK ENDS

CODE SEGMENT
	START:
	MOV AX, DATA
	MOV DS, AX
	MOV AX, STACK
	MOV SS, AX
	MOV SP, 0010H
	MOV AX, 0B800H
	MOV ES, AX

	MOV BX, 0A0H * 00
	MOV AH, 01H; 颜色
	MOV CX, 0019H
	S2:
	
	MOV SI, 0000H
	MOV DI, 0000H
	PUSH CX
	MOV CX, 0010H
	S1:

	MOV AL, [SI]	
	MOV ES:40H[BX][DI], AL; 中心位置为40H
	MOV BYTE PTR ES:40H[BX][DI].01H, AH 

	INC AH
	INC SI
	ADD DI, 0002H
	LOOP S1

	ADD BX, 00A0H
	POP CX
	LOOP S2

	MOV AX, 4C00H
	INT 21H

CODE ENDS
	END START
