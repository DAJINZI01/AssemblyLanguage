; 编程，向内存0:200 - 0:23F依次传送数据 0 - 63(3FH),程序中只能使用9条指令，9条指令中包括“MOV AX, 4C00H”和“INT 21H”。

; 下面程序的功能是将“MOV AX, 4C00H"之前的指令复制到内存0:200处。

ASSUME CS:CODE

CODE SEGMENT
	COPY_START:
	MOV AX, CODE
	MOV DS, AX
	MOV AX, 0020H
	MOV ES, AX
	MOV BX, 0000H

	MOV CX, OFFSET COPY_END - OFFSET COPY_START
	S:
	MOV DL, [BX]
	MOV ES:[BX], DL
	INC BX
	LOOP S
	COPY_END: 
	MOV AX, 4C00H
	INT 21H
CODE ENDS
	END
