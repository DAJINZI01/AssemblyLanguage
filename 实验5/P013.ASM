ASSUME CS:CODE, DS:DATA, SS:STACK

DATA SEGMENT
	DW 0123H, 0456H; 2个字型数据
DATA ENDS

STACK SEGMENT
	DW 0, 0; 栈空间
STACK ENDS

CODE SEGMENT

START:
	MOV AX, STACK
	MOV SS, AX
	MOV SP, 0010H	;栈顶偏移地址
	
	MOV AX, DATA
	MOV DS, AX
	
	PUSH DS:[0]
	PUSH DS:[2]
	POP DS:[2]
	POP DS:[0]
	
	MOV AX, 4C00H
	INT 21H
	
CODE ENDS

END START
	
; CS = X , SS = X - 16, DS = X - 32