; 将内存 FFFF0 - FFFFB 这段内存单元中的数据拷贝到 00200 - 0020B单元中

ASSUME CS:CODESG

CODESG SEGMENT

START:	
		MOV BX, 0000H
		; 循环开始
		MOV CX, 000CH
		
S:		MOV AX, 0FFFFH
		MOV DS, AX
		MOV DL, [BX]
		
		MOV AX, 0200H
		MOV DS, AX
		MOV [BX], DL
		
		INC BX
		LOOP S
	
		MOV AX, 4C00H
		INT 21H

CODESG ENDS
END START