; 用div计算data段中第一个数据除以第二个数据后的结果，商存放在第三个数据的内存单元中
ASSUME DS:DATA, CS:CODE

DATA SEGMENT
	DD 000186A1H
	DW 0064H
	DW 0000H
DATA ENDS

CODE SEGMENT
	START:
	; initiate DS
	MOV AX, DATA
	MOV DS, AX
	
	MOV AX, DS:[0000H]
	MOV DX, DS:[0002H]
	DIV WORD PTR DS:[0004H]
	MOV DS:[0006H], AX
	
	MOV AX, 4C00H
	INT 21H

CODE ENDS
	
END START